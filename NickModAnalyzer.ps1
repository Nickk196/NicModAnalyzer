function Get-SHA1($path) {
    return (Get-FileHash -Path $path -Algorithm SHA1).Hash
}

function Find-CheatString($text) {
    foreach ($p in $CHEAT_PATTERNS) {
        if ($text -match [regex]::Escape($p)) {
            return $p
        }
    }
    return $null
}

function Scan-Jar($path) {
    try {
        Add-Type -AssemblyName System.IO.Compression.FileSystem
        $zip = [System.IO.Compression.ZipFile]::OpenRead($path)

        foreach ($entry in $zip.Entries) {
            try {
                $reader = New-Object System.IO.StreamReader($entry.Open())
                $content = $reader.ReadToEnd()
                $reader.Close()

                $found = Find-CheatString $content
                if ($found) {
                    $zip.Dispose()
                    return @{ status = "CHEAT"; string = $found }
                }
            } catch {}
        }

        $zip.Dispose()
        return @{ status = "SAFE"; string = $null }
    } catch {
        return @{ status = "UNKNOWN"; string = $null }
    }
}

function Get-Modrinth($sha1) {
    try {
        $ver = Invoke-RestMethod "https://api.modrinth.com/v2/version_file/$sha1"
        if ($ver.project_id) {
            return "SAFE"
        }
    } catch {}
    return $null
}

function Scan-Mods($folder) {
    if (-not (Test-Path $folder)) { throw "Mods folder not found" }

    $jars = Get-ChildItem $folder -Filter *.jar
    $results = @()

    foreach ($jar in $jars) {
        Write-Host "Scanning -> $($jar.Name)" -ForegroundColor White

        $sha1 = Get-SHA1 $jar.FullName

        $modrinthStatus = Get-Modrinth $sha1
        if ($modrinthStatus -eq "SAFE") {
            Write-Host "  [SAFE] (Verified on Modrinth)" -ForegroundColor Green

            $results += [PSCustomObject]@{
                file = $jar.Name
                sha1 = $sha1
                status = "SAFE"
                cheat_string = $null
            }
            continue
        }

        $scan = Scan-Jar $jar.FullName

        switch ($scan.status) {
            "CHEAT" {
                Write-Host "  [CHEAT DETECTED] -> $($scan.string)" -ForegroundColor Red
            }
            "SAFE" {
                Write-Host "  [UNKNOWN] (No cheats found, not on Modrinth)" -ForegroundColor Yellow
                $scan.status = "UNKNOWN"
            }
            default {
                Write-Host "  [UNKNOWN] (Unreadable or invalid JAR)" -ForegroundColor DarkYellow
                $scan.status = "UNKNOWN"
            }
        }

        $results += [PSCustomObject]@{
            file = $jar.Name
            sha1 = $sha1
            status = $scan.status
            cheat_string = $scan.string
        }
    }

    return $results
}

